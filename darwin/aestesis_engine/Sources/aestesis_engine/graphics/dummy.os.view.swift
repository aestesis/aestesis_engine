import Foundation
import MetalKit

//////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////////
public class DummyOsView: SystemView {
    //////////////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////////////////////////////////////
    public var viewport: Viewport?
    var timer: Foundation.Timer?
    var fps:Double = 60
    //////////////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////////////////////////////////////
    init() {
        viewport = Viewport(systemView: self, device: MTLCreateSystemDefaultDevice()!, size: Size.zero)
        start()
    }
    public required init(coder: NSCoder) {
        viewport = Viewport(systemView: self, device: MTLCreateSystemDefaultDevice()!, size: Size.zero)
        start()
    }
    deinit {
        self.timer?.invalidate()
        self.viewport?.detach()
    }
    //////////////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////////////////////////////////////
    func start() {
        let _ = Thread(name: "Composition") {
            let runLoop = RunLoop.current
            self.setTimer()
            runLoop.run()
        }
    }
    
    func setTimer()  {
        let runLoop = RunLoop.current
        self.timer?.invalidate()
        self.timer = Foundation.Timer(fireAt: Date(), interval: 1.0 / self.fps, target: self, selector: #selector(self.pulse), userInfo: nil, repeats: true)
        runLoop.add(self.timer!, forMode: .common)
    }
    
    @objc
    func pulse() {
        if let timer = timer, timer.timeInterval != 1.0 / self.fps {
            Debug.info("DummyOsView: timer set at \(self.fps) fps")
            setTimer()
        }
        viewport!.update()
    }
    //////////////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////////////////////////////////////
    public var pauseRefresh: Bool {
        get { return true }
        set {}
    }
    public func focus() {
    }
    public func toggleFullScreen() {
    }
    public func captureBackButton(_ capture: Bool) {
    }
    public var title: String {
        get { return "" }
        set {}
    }
    public var orientation: Orientation {
        return Orientation.portrait
    }
    //////////////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////////////////////////////////////
}
