//////////////////////////////////////////////////////////////////////////////////////////////////////////
class Shades : Reflex {
    //////////////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////////////////////////////////////
    var texture:Bitmap?
    var vv=[(phase:Double,p:Point)]()
    var color = Color.black
    //////////////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////////////////////////////////////
    override init(parent: NodeUI) {
        super.init(parent:parent)
        texture = Bitmap(parent:self,size:Size(256,256))
        for _ in 0..<10 {
            vv.append((phase: ß.rnd*ß.π*2,p:Point(ß.rnd*0.2+0.9,ß.rnd*0.2+0.9)))
        }
    }
    //////////////////////////////////////////////////////////////////////////////////////////////////////////
    override func detach() {
        texture?.detach()
        texture = nil
        super.detach()
    }
    //////////////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////////////////////////////////////
    override func draw(graphics g:Graphics,rect:Rect,time:Double,audio:AudioAnalyzer.Info,power:Double) {
        if let texture = texture {
            let ge = EffectGraphics(parent:self,graphics:g)
            color = color * 0.9 + (Color.aeOrange*Double(audio.eq.low)+Color.aeAqua*Double(audio.eq.medium)+Color.aeViolet*Double(audio.eq.high*2)) * 0.05
            ge.drawPopoo(rect:rect.fit(1),source:texture,offset:Point(-(time*0.47678656).truncatingRemainder(dividingBy: 10),0),amplitude:Point(4+sin(time*0.05767613)*2,10),decal:Point(0,sin(time*0.01616751)*0.1),blend:.difference,color:Color(a:power,rgb:color.saturated))
            let gl = Graphics(image:texture)
            gl.fill(rect: texture.bounds, blend: .sub, color: Color(a:1,l:0.1))
            drawLine(gl,rect: texture.bounds,power:1,time:time)
        }
    }
    //////////////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////////////////////////////////////
    func drawLine(_ g:Graphics,rect:Rect,power:Double,time:Double) {
        let t = time * 0.213
        let path = Path()
        var pp = [Point]()
        var i = 0
        let np : ()->(Int)  = {
            let o = i
            i += 1
            return o
        }
        for v in vv {
            pp.append(Point(sin(v.phase+t*v.p.x)*0.5+0.5,sin(v.phase+t*v.p.y)*0.5+0.5) * rect.size.scale(1.5) + rect.scale(1.5).origin )
        }
        path.append(Path.Segment.moveTo(pp[np()]))
        while i < pp.count {
            path.append(Path.Segment.cubicTo(pp[np()], pp[np()], pp[np()]))
        }
        path.append(Path.Segment.close())
        let paint = Paint(parent: self, mode: .stroke, blend: .add, color: Color(a:1,l:0.3*power))
        paint.strokeWidth = 20
        g.draw(path,paint)
    }
    //////////////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////////////////////////////////////
}
